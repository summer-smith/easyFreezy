            <h5>Q: How long can I keep my freezer meals?</h5>
            <p><b>A: </b>Frozen meals are considered best if kept for
            less than six months.  That said, some things keep longer than 
            others.  When in doubt, stick to the six-month rule, but 
            you will probably be just fine if you keep something in your freezer for 
            longer.  The worst consequence will likely be dry, frost-bitten food.</p>        
            <br>
        </div>
        
        <div>
            <h5>Q: Does freezing food make it safe to it?</h5>
            <p><b>A: </b>Not necessarily.  Although freezing food will stop
                bacteria and yeast from growing, they are not dead.  They can
                grow again if the food is defrosted improperly.</p>  
            <br>
        </div>
        
        
        <div>
            <h5>Q: So how do I defrost my meals?</h5>
            <p><b>A: </b>When defrosting, you need to prevent bacteria from
                growing.  To do this, you can defrost items slowly in the 
                refrigerator overnight or over a few days.</p>
            <p>You can defrost a little faster by sealing your food in a ziplock
                bag and immersing it in cold water, or running cold water over it
                in the sink.</p>  
            <p>If you are really in a hurry, you can microwave food, but you 
                need to cook it immediately afterwards.</p>        
            <br>
        </div>
        

