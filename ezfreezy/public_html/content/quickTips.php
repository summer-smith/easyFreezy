        <h4>Pre-cooking meat</h4>
        <p>This is a <b>huge</b> time saver!  Pre-cook your meet so that 
        it is ready to throw in to any recipe.  I like to do this with
        small portions or chunks of meat.  I think that large meat portions
        (think full chicken breasts, steaks, or roasts) are best cooked
        fresh, but you can definitely freeze them raw for fresh cooking later!</p>
        <p>Here are some basic tips and tricks for freezing meat:</p>
        <ul class="content">
            <li>Buy your meat in bulk (I'm talking 5 to 10 pounds here!). </li> 
            <li>Split it up and cook it in one or two-pound increments. </li>
            <li>Add different seasonings!  If you have a variety, you can use your
                meat in a variety of ways.  I always like to leave at least 
                one batch plain.</li>
            <li>Let your meat cool, then split into quart-sized freezer bags.</li>
            <li>Label your meat!  Make sure to indicate the date and 
                what kind of seasoning you used for each batch.</li>
            <li>Chicken: boil or pan-sear your chicken.  You can cut/shred it before
                or after cooking, but it's best to freeze it shredded or cubed.</li>
            <li>Ground beef: add taco seasoning, soup seasoning, or leave it plain
                to throw in to hamburger helper! You can also make meatballs,
                hamburger patties, or meatloaf and freeze those. </li>
            <li>Stew meat: brown your meat and stick it in the freezer.  When
                you feel like stew, your meat is ready to go!</li>
            <li>Sausage and bacon: cook your ground sausage, sausage links, and bacon 
                ahead of time, then re-heat for breakfast!</li>
            <li>Buy pre-cooked!  A whole, cooked chicken is cheaper than an uncooked one.
                Buy a rotisserie chicken, cut it up, and freeze it!</li>
        </ul>
        <p><i>Note:</i> always be sure to label your meat!  Indicate the date and 
                what kind of seasoning you used for each batch.</p>
      
        <hr class="lightBlue"/>
        
        <h4>Pre-cook breakfast</h4>
        <p>Basically all breakfast items freeze well, with the exception of eggs.</p>
        <ul class="content">
            <li><b>Hash browns:</b> cook, re-heat in a little bit of butter or oil</li>
            <li><b>Pancakes and waffles:</b> use a toaster or oven to re-heat, since
                a micro-wave will leave them soggy.</li>
            <li><b>Muffins:</b> make big batches and freeze in portions of 2 or 4,
                then pull out before bed to de-thaw over night.</li>
        </ul>
      
        <hr class="lightBlue"/>
        
        <h4>Buy in bulk</h4>
        <p>I've mentioned this several times, but there are alot of items you can
            buy in bulk, then freeze until you are ready to use.  <b>Save yourself
            a trip to the grocery store!</b></p>
        <ul class="content">
            <li><b>Milk-</b> If your freezer is small, freeze one gallon at a time and rotate 
                frozen gallons back into the fridge.  They will de-thaw slowly.</li>
            <li><b>Meat- </b>even if you don't cook it, buy it on sale and freeze the excess!</li>
            <li><b>Some un-cooked veggies-</b> Potatoes, carrots, spinach...dice, then freeze them
                so they are ready to throw in to your next recipe!  Just be sure to 
                avoid veggies that 
                <a href="/content/whatDoesntFreezeWell.php" title="veggies to avoid">don't freeze well.</a>
            </li>
            <li><b>Juice- </b>Just make sure the container is big enough, or it will crack when
                    the juice freezes and expands!</li>
             <li><b>Most bread products- </b>Tortillas, bread loaves, muffins, buns, dinner rolls....</li>
        </ul>
            
        <hr class="lightBlue"/>
            
        <h4>Soups and Sauces</h4>
        <ul class="content">
            <li><b>Soups and stews</b>- and water-based soup or stew can be frozen and 
                re-heated!  Cook a double or triple batch, and freeze the extra.
                Cream or milk based soups don't freeze well.</li>
            <li><b>Sauces</b>- spaghetti sauces and roux sauces freeze great and stay  
                fresh for up to 6 months!  Next time you whip some up, make a 
                little extra to freeze for another day.</li>
        </ul>
        
