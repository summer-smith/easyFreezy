        <h4>Marinated Pork Chops</h4>
        <hr class="lightBlue"/>
        
        <h5>Ingredients:</h5>
        <ul>
            <li>4-5 cloves garlic, minced</li>
            <li>1/2 teaspoon salt</li>
            <li>1/2 teaspoon black pepper</li>
            <li>1 and 1/2 teaspoon balsamic vinegar</li>
            <li>2 teaspoons olive oil</li>
            <li>1-2 pounds pork chops</li>
        </ul>
        
        <h5>Freezing:</h5>
        <p>Combine all ingredients in a ziplock bag.  Mix marinade well, squeeze out
            exess air, and freeze.
         <i>Note:</i> Freeze chops in reasonable portions.</p>
        
        <h5>Cooking:</h5>
        <p><i>Grill:</i></p>
        <p>Remove pork from bags.  Grill until cooked through, 15-30 minutes depending on thickness.</p>
        <p><i>Bake:</i></p>
        <p>Preheat oven to 400*.  Remove pork from bag and place
            in a foil-lined baking dish.  Bake 20-25 minutes.</p>
        
