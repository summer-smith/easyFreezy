        <h4>Chicken Noodle Soup</h4>
        <hr class="lightBlue"/>
        
        <h5>Ingredients:</h5>
        <ul>
            <li><i>Frozen:</i></li>
            <li>1 to 2 pounds chicken, shredded or cubed</li>
            <li>1 package pasta noodles, egg noodles, OR 2 cups rice</li>
            <li>2-3 sliced carrots</li>
            <li>2-3 potatoes, peeled and cubed</li>
            <li><i>Fresh:</i></li>
            <li>1 yellow onion</li>
            <li>4 cups water</li>
            <li>Chicken bouillon, to taste</li>
            <li>1 package pasta noodles, egg noodles, OR 2 cups rice</li>
        </ul>
        
        <h5>Freezing:</h5>
        <p>Boil chicken.  Cut up vegetables.  Freeze all separately.</p>
        
        <h5>Cooking:</h5>
        <p>Dice onion.</p>
        <p><i>Slow cooker:</i> combine all ingredients except pasta.  Cook
            on low for 5-6 hours, or until veggies are tender.</p>
        <p>C<i>Stove top: </i> combine all ingredients except pasta.  Boil 
        for 1-2 hours, until veggies are tender.</p>
        <p>Add rice/pasta, cook an additional 15 minutes or until rice/pasta
        is tender.</p>
        
