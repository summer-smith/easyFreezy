        <h4>Chicken, Rice, and Sausage Casserole</h4>
        <hr class="lightBlue"/>
        
        <h5>Ingredients:</h5>
        <ul>
            <li>1 to 2 pounds chicken, shredded or cubed</li>
            <li>1 package sausage</li>
            <li>2 cups rice</li>
            <li>4 cups water</li>
            <li>2 tablespoons chicken bouillon</li>
        </ul>
        
        <h5>Freezing:</h5>
        <p>Brown sausage, boil or pan-sear chicken.  Freeze individually or 
            combine, with rice, in a gallon-sized ziplock back or foil baking 
            pan.</p>
        
        <h5>Cooking:</h5>
        <p>Pre-heat oven to 350*.</p>
        <p>Combine sausage, chicken, and rice in casserole dish (if necessary).
            Combine water and rice and add.  Mix.  Bake for 30-45 minutes, until
            chicken is heated through and rice in tender.</p>
        
