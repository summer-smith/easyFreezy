            <li>1/2 pound ground beef</li>
            <li>1 14 oz. can diced stewed tomatoes</li>
            <li>4 carrots, sliced</li>
            <li>2-3 potatoes, cubed</li>
            <li>1 bag frozen peas</li>
            <li><i>Optional:</i></li>
            <li>1 can french-cut greebeans (drained)</li>
            <li>1 can sliced mushrooms</li>
