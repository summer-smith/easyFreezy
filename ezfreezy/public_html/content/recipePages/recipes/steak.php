        <h4>Marinated Stake</h4>
        <hr class="lightBlue"/>
        
        <h5>Ingredients:</h5>
        <ul>
            <li>2 to 3 pounds stake</li>
            <li>2 tablespoons soy sauce</li>
            <li>1 tablespoon stake seasoning <i>Montreal Stake Seasoning preferred</i></li>
            <li>2 tablespoons olive oil</li>
        </ul>
        
        <h5>Freezing:</h5>
        <p>Combine ingredients in ziplock bag.  Combine well, squeeze out excess air, freeze.</p>
        
        <h5>Cooking:</h5>
        <p>Grill until cooked through, 15-20 minutes depending on thickness.</p>
        
