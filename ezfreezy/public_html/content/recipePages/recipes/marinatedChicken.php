        <h4>Marinated Chicken</h4>
        <hr class="lightBlue"/>
        
        <h5>Ingredients:</h5>
        <ul>
            <li>2 to 3 pounds boneless, skinless chicken breast</li>
            <li>1 1/2 cups lemon-lime soda <i>(think seven-up or sprite)</i></li>
            <li>1/2 cup olive oil</li>
            <li>1/2 cup soy sauce</li>
            <li>1/4 teaspoon garlic powder</li>
        </ul>
        
        <h5>Freezing:</h5>
        <p>Combine ingredients in ziplock bag.  Combine well, squeeze out exess air, freeze.
        
        <h5>Cooking:</h5>
        <p>Grill until cooked through, 15-20 minutes depending on thickness, or until
            juices run clear..</p>
        
