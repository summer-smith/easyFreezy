        <h3>The Basics</h3>
        <hr class="lightBlue"/>
        <p>If your just trying to get an idea of how to get in to the freezer
        meal game, read on!  Here is where you should start if you are new to 
        to freezer meals.</p>
                
        <hr class="lightBlue"/>
        
        <img src="/images/articleImages/theBasics.png" style="width:40%" alt="Filler Image">
        
        <h4>Step 1:  Supplies</h4>
        <p>If you have never made freezer meals before, there are a few things
        you will need.</p>
        <ol>
            <li><b>Freezing pan</b>- I use a jelly roll pan, but any kind of 
                baking sheet will do.  This will be used for flash-freezing small
                food items, like cookies.</li>
            <li><b>Freezer bags</b>- Name brand or off brand zipper bags.  Just
                make sure they have a double zipper on the top to prevent freezer
                burn!  <b>Gallon</b> and <b>quart</b> sized bags will be useful.</li>
            <li><b>Foil baking pans</b>- Foil pans are ideal for freezing and
                baking casseroles and other messy dishes.</li>
            <li><b>Permanant marker</b>- for labeling your food!  Dating is 
                important.  Even frozen food can go bad!</li>
            <li><b>Cooking supplies</b>- Measuring cups, mixing bowls, 
                knife, cutting board...just the basics for meal preparation.</li>
        </ol>
        
        <h4>Step 2: Recipes</h4>
        <p>There are lots of freezer-friendly recipes out there!  Feel free to
            try some of the <a href="/content/recipes.php" title="recipes">recipes</a>
            listed here on Easy Freezy, or check out our <a href="/content/otherSources" title="otherSources">other sources</a>
            page for links to other freezer sites! </p>
        <p>You can come up with your own recipes, but first, it's important to get an
            idea of <a href="/content/whatFreezesWell.php" title="whatFreezesWell">what freezes well</a>
            and <a href="/content/whatDoesntFreezeWell.php" title="whatDoesn'tFreezeWell">what doesn't</a>.</p>
        <p>Keep in mind that whatever recipes you pick, you will be eating several 
            times, so make sure to pick something you and the family will like!</p>
        
         <h4>Step 3: Creating a Shopping List</h4>
         <p>Once you know what you're going to be making, sit down and write
            out all the ingredients you are going to need.</p>
        
        <h4>Step 4: Shopping</h4>
        <p>Go forth and purchase your food!</p>
         
         <h4>Step 5: Cooking</h4>
         <p>Depending on how many meals you are making, this could take several 
            hours or an entire day!  You could just do a little bit every day
            for a few weeks.  Whatever you choose, plan ahead, grab a snack, and 
            get ready to cook!</p>
         <ol>
            <li>Start by chopping all fresh fruit and vegetable ingredients.  Grate 
                your cheese, too!</li>
            <li>Cooking, cutting, and prepping all your meat is the next step.
                Keep in mind that some recipes call for the meat un-cooked</li>
            <li>Do any of your other ingredients need cooked? Do that now!</li>
            <li>Mix everything together as per recipe instructions. Slow-cooker and
                non-bake recipes are usually best stored in ziplock bags or tupperware, while casseroles
                and baking dishes should go in your foil baking pans.</li>
            <li>Label, label, label!  Mark down the day you made it, along with 
                baking instructions.  If anything needs to thaw before cooking,
                make sure to mark that down as well!</li>
            <li>Finally, pop all your delicious meals into your freezer!</li>
         </ol>
         
         <h4>Step 5: Revel in the Easiness</h4>
         <p>Now that you have freezer meals at the ready, use them!  Most 
             freezer meal recipes require minimal prep time, making them ideal 
             for crazy busy days.  Make sure to rotate your freezer meals, so 
             that nothing gets wasted!</p>
