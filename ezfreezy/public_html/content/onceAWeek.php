        <p>Want to reduce the amount of time you spend in the kitchen?
            Here is a quick guide to creating a resource of delicious
            freezer meals while cooking only once a week!</p>
        
        <hr class="lightBlue"/>
        <div>
            <h4>What you need to know:</h4>
            <p>The key to once a week cooking is making an extra-large dinner, and 
                freezing the excess for another meal.</p>
            <p>If you really, truly want to follow this plan, it is a s-l-o-w process
                for filling up your freezer!  Plus, for a couple of weeks, you will have
                no variety, since whatever you made will be the only thing in your freezer all week.
                However, it's low-key and relatively 
                minimal effort.  If you really want to stock you freezer, it is more
                ideal to start with a big, 
                <a href="/content/onceAMonth.php" title="once a month cooking">once a month cook-off</a>
                and then use the once-a-week method to keep your supply of freezer meals up.</p>
        </div>
        
        <hr class="lightBlue"/>
        
        <div>
            <h4>How to do it:</h4>
            <div class="recipes">
                <ol>
                    <li><b>Pick</b>
                    <br>You need to pick 1) a day of the week and 2) a yummy recipe to make!
                    If you don't have the ingredients on hand, make sure to pick up enough
                    for three or four times the recipe- however many extra you make, you
                    get to freeze.</li>
            
                    <li><b>Make</b>
                    <br>On you designated freezer meal making day, make your meal and however
                        many extra batches you planned for.  Make one batch to eat for dinner, 
                        and freeze the extras.</li>
            
                    <li><b>Bake</b>
                    <br>If you started with a big, one-time once-a-month freeze-off, then 
                        you can start enjoying your meals whenever.  However, if you are sticking 
                        exclusively with the once-a-week scheme, then don't use any of your 
                        freezer meals for the first four weeks.  After four weeks, you've got 
                        a little more variety, and you can start using your meals throughout
                        the week without depleting your stores.</li>
                </ol>
