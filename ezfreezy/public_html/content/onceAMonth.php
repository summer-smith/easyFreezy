        <h3>Cooking Once a Month</h3>
        <img src="/images/articleImages/onceAMonth.png" style="width:40%" alt="Filler Image">
        <p>Want to reduce the amount of time you spend in the kitchen?
            Here is a quick guide to creating a resource of delicious
            freezer meals while cooking only once a month!</p>
    
        <hr class="lightBlue"/>
        <div>
            <h4>What you need to know:</h4>
            <p>Doing a big, once-a-month freeze-off will get your freezer stocked 
                tight with only one (very long) day of cooking.  This is a great way 
                to jump-start your freezer stock pile.  To keep it up, you can
                continue cooking once each month, or you can try
                <a href="/content/onceAWeek.php" title="once a week cooking">cooking once a week</a>,
                which is a little more low-key.</p>
        </div>
        
        <hr class="lightBlue"/>
        
        <div>
            <h4>How to do it:</h4>
            <div class="recipes">
                <ol>
                    <li><b>Pick</b>
                    <br>Pick how many recipes you want to make, and how
                        many of each. I usually aim for seven or eight 
                        recipes, and four batches of each.</li>
                    
                    <li><b>Shop</b>
                        <br>In addition to the necessary groceries to complete your meals, you
                         will want to pick up foilware baking pans and ziplock bags.
                        Make a list of everything your recipes call for, by 
                        ingredient.  If six recipes call for one onion each,
                        buy six onions!</li>
            
                    <li><b>Clean</b>
                        <br>Since you are about to make a mess, start with a 
                        clean kitchen.  Any dishes you might need for cooking,
                        measuring spoons, cutting boards, ect. should be clean
                        and ready to use!</li>
                    
                    <li><b>Make</b>
                        <br>Cook and prep items in batches.  Chopping  
                        carrots?  Chop all of them!  Boiling chicken?
                        Boil as much as your recipes call for.</li>
            
                    <li><b>Freeze</b>
                        <br>Once everything is assembled and cooked, pop it in 
                        the freezer!  Freezing things flat will save you room,
                        and makes your meals more organized and accessible.</li>
                    
                    <li><b>Bake</b>
                        <br>Now that you have a nice little freezer stock pile,
                        pull out a freezer meal four or five nights a week!  Although
                        some recipes keep longer, it's best to try and use 
                        your meals within six months.</li>
                </ol>
            </div>
